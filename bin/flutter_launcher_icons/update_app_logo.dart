// ignore_for_file: avoid_print

import 'dart:io';

void main() {
  final logosDir = Directory('assets/logos');
  final appLogoFile = File('lib/core/types/app_logo.dart');

  // Get all logo directories
  final logoNames = <String>[];
  for (final entity in logosDir.listSync()) {
    if (entity is Directory && entity.path.endsWith('.icon')) {
      final logoName = entity.path.split('/').last.replaceAll('.icon', '');
      logoNames.add(logoName);
    }
  }

  // Sort logo names to ensure consistent ordering
  logoNames.sort();

  if (logoNames.isEmpty) {
    print('Error: No logo directories found in assets/logos/');
    exit(1);
  }

  print('Found ${logoNames.length} logos:');
  for (final name in logoNames) {
    print('  - $name');
  }

  // Generate enum entries
  final enumEntries = <String>[];
  for (final logoName in logoNames) {
    final enumName = _toEnumName(logoName);
    enumEntries.add("  $enumName(logoName: '$logoName')");
  }

  // Generate switch cases
  final switchCases = <String>[];
  for (final logoName in logoNames) {
    final enumName = _toEnumName(logoName);
    final assetPath = _toAssetPath(logoName);
    switchCases.add("      case .$enumName:\n        return $assetPath;");
  }

  // Generate the complete file content
  final fileContent =
      '''// GENERATED FILE - DO NOT MODIFY
// This file is auto-generated by bin/update_app_logo
// To add or remove logos, modify the script or update assets/logos/ directory

import 'package:storypad/gen/assets.gen.dart';

enum AppLogo {
${enumEntries.join(',\n')}
  ;

  final String logoName;

  String get xcodeLogoName => logoName;
  String get androidActivityAliasName => 'com.tc.writestory.\$logoName';

  bool get free => this == AppLogo.storypad_1_0;

  const AppLogo({
    required this.logoName,
  });

  AssetGenImage get asset {
    switch (this) {
${switchCases.join('\n')}
    }
  }
}
''';

  // Write to file
  appLogoFile.writeAsStringSync(fileContent);
  print('\n✅ Successfully updated ${appLogoFile.path}');
  print('   Generated ${logoNames.length} logo entries');
}

/// Convert logo name to enum name
/// Example: storypad_logo_1_0 -> storypad_1_0
String _toEnumName(String logoName) {
  return logoName.replaceAll('storypad_logo_', 'storypad_');
}

/// Convert logo name to Assets path
/// Example: storypad_logo_1_0 -> Assets.logos.storypadLogo10.assets.storypadLogo10
String _toAssetPath(String logoName) {
  final camelCase = _toCamelCase(logoName);
  return 'Assets.logos.$camelCase.assets.$camelCase';
}

/// Convert snake_case to camelCase
/// Example: storypad_logo_1_0 -> storypadLogo10
String _toCamelCase(String input) {
  final parts = input.split('_');
  final result = StringBuffer(parts[0]);

  for (var i = 1; i < parts.length; i++) {
    final part = parts[i];
    if (part.isNotEmpty) {
      result.write(part[0].toUpperCase());
      if (part.length > 1) {
        result.write(part.substring(1));
      }
    }
  }

  return result.toString();
}
